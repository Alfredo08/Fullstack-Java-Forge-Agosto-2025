package clases;

public class FuncionesAritmeticas {
    public static double sumaDosNumeros(double num1, double num2){
        return num1 + num2;
    }

    public static double restaDosNumeros(double num1, double num2){
        return num1 - num2;
    }

    public static double multiplicaDosNumeros(double num1, double num2){
        return num1 * num2;
    }

    public static double divideDosNumeros(double num1, double num2){
        return num1 / num2;
    }
}
